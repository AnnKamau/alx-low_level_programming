#include "3-calc.h"

/**
 * op_add - return the sum of two integers
 * @a: first integer
 * @b: second integer
 * Return: sum of integers
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Returns the difference of two integers
 * @a: first integer
 * @b: second integer
 * Return: the difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - the product of two integers
 * @a: first integer
 * @b: second integer
 * Return: product of two integers
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - the quotient of two integers
 * @a: first integer
 * @b: second integer
 * Return: the quotient of two integers
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Remainder of division of two integers
 * @a: first integer
 * @b: second integer
 * Return: remainder of division of two integers
 */

int op_mod(int a, int b)
{
	return (a % b);
}
